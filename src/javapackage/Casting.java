package javapackage;

public class Casting {

	public static void main(String[] args) {
       
		double b = 5.0;
		
		int a = (int) b;
		
		System.out.println(b);
		System.out.println(a);
		
		String s = "10";
		int i = Integer.parseInt(s);
		System.out.println(i);
		
		int e=5;
		
		String t = String.valueOf(e);
		System.out.println(t);
		
	}

}
